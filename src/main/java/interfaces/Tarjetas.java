    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String numeroTarjeta = txtnumeroTarjeta.getText().trim();
    String vencimiento = txtvencimiento.getText().trim();
    String codigoCVV = txtcodigoCVV.getText().trim();
    String nombreTitular = txtnombreTitular.getText().trim();
    String apellidoTitular = txtapellidoTitular.getText().trim();
    String dni = txtdni.getText().trim();
    String correo = txtcorreo.getText().trim();
    
    // Validar campos vacíos
    if (numeroTarjeta.isEmpty() || vencimiento.isEmpty() || codigoCVV.isEmpty() || nombreTitular.isEmpty() || apellidoTitular.isEmpty() || dni.isEmpty() || correo.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
        return;
    }
    
    // Validar el formato del número de tarjeta
    if (!numeroTarjeta.matches("\\d{4} \\d{4} \\d{4} \\d{4}")) {
        JOptionPane.showMessageDialog(this, "El número de tarjeta debe tener el formato 0000 0000 0000 0000.");
        return;
    }

    // Validar el formato del CVV
    if (!codigoCVV.matches("\\d{3}")) {
        JOptionPane.showMessageDialog(this, "El código CVV debe tener 3 dígitos.");
        return;
    }

    // Validación del vencimiento (formato MM/AA)
    if (!vencimiento.matches("\\d{2}/\\d{2}")) {
        JOptionPane.showMessageDialog(this, "El vencimiento debe tener el formato MM/AA.");
        return;
    }
    
    // Validación de vencimiento de tarjeta
    try {
        String[] parts = vencimiento.split("/");
        int mes = Integer.parseInt(parts[0]);
        int anio = Integer.parseInt(parts[1]);
        if (mes < 1 || mes > 12) {
            JOptionPane.showMessageDialog(this, "El mes de vencimiento debe estar entre 01 y 12.");
            return;
        }

        // Fecha actual
        java.util.Calendar now = java.util.Calendar.getInstance();
        int currentYear = now.get(java.util.Calendar.YEAR) % 100;
        int currentMonth = now.get(java.util.Calendar.MONTH) + 1; // Mes es 0-indexado

        // Verificación
        if (anio < currentYear || (anio == currentYear && mes < currentMonth)) {
            JOptionPane.showMessageDialog(this, "La tarjeta ha expirado.");
            return;
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El vencimiento debe tener el formato MM/AA.");
        return;
    }

    guardarDatosTarjeta(numeroTarjeta, vencimiento, codigoCVV, nombreTitular, apellidoTitular, dni, correo);
    dispose();
    new ConfirmacionDeCompra().setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

