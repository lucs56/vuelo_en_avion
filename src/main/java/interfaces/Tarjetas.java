private void guardarDatosTarjeta(String numeroTarjeta, String vencimiento, String codigoCVV, String nombreTitular, String apellidoTitular, String dni, String correo) {
        

        String sql = "INSERT INTO tarjetas (numero_tarjeta, vencimiento, codigo_cvv, nombre_titular, apellido_titular, dni, correo ) VALUES ( ?, ?, ?, ?, ?, ?, ?)";

        try {
            PreparedStatement preparedStatement = cn.prepareStatement(sql);
            preparedStatement.setString(1, numeroTarjeta);
            preparedStatement.setString(2, vencimiento);
            preparedStatement.setString(3, codigoCVV);
            preparedStatement.setString(4, nombreTitular);
            preparedStatement.setString(5, apellidoTitular);
            preparedStatement.setString(6, dni);
            preparedStatement.setString(7, correo);
           

            int filasAfectadas = preparedStatement.executeUpdate();

            if (filasAfectadas > 0) {
                JOptionPane.showMessageDialog(this, "Datos de la tarjeta guardados exitosamente.");
            } else {
                JOptionPane.showMessageDialog(this, "Error al guardar los datos de la tarjeta.");
            }

            cn.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos.");
        }
    }


